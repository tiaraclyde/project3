package org.cs213.clinic.core;
/**
 * This enum represents the different types of imaging services that can be
 * provided in a radiology department. The imaging services are used to
 * determine the type of imaging service that will be.
 *
 * @author Elian Deogracia-Brito
 * @author Tiara Clyde
 */
public enum Radiology {
    /** The imaging service is a CAT scan. */
    CATSCAN,
    /** The imaging service is an MRI. */
    ULTRASOUND,
    /** The imaging service is an X-ray. */
    XRAY
}

